package Conditionalstatement;

public class ifElseifCondition {

	public static void main(String[] args) {
		int marks = 72;
		
		if (marks>=90) {
			
			System.out.println("A+ Grade");
			
		}
		else if (marks>=80) {
			
			System.out.println("A Grade");
			
		}	
		else if (marks>=70) {
			
			System.out.println("B Grade");
			
		}
		else if (marks>=60) {
			
			System.out.println("C grade");
			
		}
		else {
			System.out.println("Failed");
		}
		

		}
		


	

	}
