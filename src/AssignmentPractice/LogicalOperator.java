package AssignmentPractice;

public class LogicalOperator {

	public static void main(String[] args) {
		/* Check if two numbers are both positive using the AND (&&) operator. Example Input: a = 12, b = -5 
        Condition: a > 0 && b > 0 Example Output: false */

		int a=12;
		int b=-5;
		
		if (a>0 && b>0) {
			System.out.println(true);
		}
		else {
			System.out.println(false);
		}

	}

}
