package AssignmentPractice;

public class AddSubMulDivMod {

	public static void main(String[] args) {
		// Write a Java program to perform addition, subtraction, multiplication, division, and modulus on two numbers (10 and 5) and display the results.

		int a=10;
		int b=5;
		
		int add=a+b;
		int Sub=a-b;
		int mul=a*b;
		double div=a/b;
		double mod=a%b;
		
		System.out.println(add);
		System.out.println(Sub);
		System.out.println(mul);
		System.out.println(div);
		System.out.println(mod);
	}

}
